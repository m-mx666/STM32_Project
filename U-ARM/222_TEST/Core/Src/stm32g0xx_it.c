/**
  * @brief This function handles USART1 global interrupt / USART1 wake-up interrupt through EXTI line 25.
  */
void USART1_IRQHandler(void)
{
  /* USER CODE BEGIN USART1_IRQn 0 */
/* 检查并清除错误标志 (ORE, FE, NE) */
    if (USART1->ISR & (USART_ISR_ORE | USART_ISR_FE | USART_ISR_NE)) {
        USART1->ICR = USART_ICR_ORECF | USART_ICR_FECF | USART_ICR_NECF;
        HAL_UART_DMAStop(&huart1);
        HAL_UART_Receive_DMA(&huart1, g_chain_frame, FRAME_SIZE);
        return;
    }

    /* 处理空闲中断 */
    if (USART1->ISR & USART_ISR_IDLE) {
        USART1->ICR = USART_ICR_IDLECF; // 清除空闲中断标志
        
        HAL_UART_DMAStop(&huart1);
        uint16_t len = FRAME_SIZE - __HAL_DMA_GET_COUNTER(huart1.hdmarx);
        
        if (len == FRAME_SIZE && g_chain_frame[0] == 0xAA) {
            g_rx_ready = 1; // 标记接收成功
        } else {
            HAL_UART_Receive_DMA(&huart1, g_chain_frame, FRAME_SIZE);
        }
    }      /* USER CODE END USART1_IRQn 0 */
  HAL_UART_IRQHandler(&huart1);
  /* USER CODE BEGIN USART1_IRQn 1 */

  /* USER CODE END USART1_IRQn 1 */
}

